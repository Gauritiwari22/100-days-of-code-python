# Functions- a block of code defined to perform a particular tak which can be called 
# again and again to perform the same task without writing the same code

#user defined functions
def summation(a,b): # defining the function
    print(a+b)      # mentioning the condition
summation(6,5)      # calling the function

# While loops- while something is true keep running until there is a false condition
i=0
while i<=10:
    print(i)
    i+=1

# Infinite loop = loops that runs forever for a given condition
while 5>3:
    print("TRUE")
